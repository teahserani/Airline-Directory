int sum_if_a_palindrome(int integers[], int length)
{
	int tot=0;
	for(int i=0; i<length; i++)
	{
		tot=tot+integers[i];
	}
	return tot;
}
bool is_a_palindrome(int integers[], int length)
{
	bool palindrome=false;
	for(int i=0; i<length; i++)
	{
		for(int j=length; j>length; j--)
		{
			if(integers[i]==integers[j])
			{
				palindrome=true;
			}
		}
	}
	return palindrome;
}
