
#include <iostream>
using namespace std; 

int *readNumbers(int n) 
{
	int *array= new int[n];
	for (int i=0; i<n; i++)
	{
		cin>>array[i];
	}
	return array;
}
void printNumbers(int *numbers,int length)
{
	for (int i=0; i<length; i++)
	{
		cout<<i; 
		cout<<" ";
		cout<<numbers[i]<<endl;
	}
}
int *addArrays(int *numbers1,int *numbers2,int length) 
{
	int *array = new int [length];
	for (int i=0; i<length; i++)
	{
		cin>>array[i];
	}
	for(int i=0; i<length; i++)
	{
		array[i]=numbers1[i]+numbers2[i];
	}
	return array;
}
	

